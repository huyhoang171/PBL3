
function showLoginForm() {
    var loginForm = document.getElementById("login-form");
    loginForm.style.display = "block"; // Hiển thị form đăng nhập
}

function closeLoginForm() {
    var loginForm = document.getElementById("login-form");
    loginForm.style.display = "none"; // Ẩn form đăng nhập
}


